// Pace launches to an input-tokens/minute budget so big batches don't trip the
// provider's per-minute rate limit (which would surface as [generation error]).
// Each note ≈ 3 large passes; gate every note on a refilling token bucket. Tune
// TPM to the model's tier — observed limits: Claude Sonnet 30k, Haiku 50k; Opus
// and the OpenAI tier ran clean at the default. Set TPM=0 to disable pacing.
const TPM = process.env.TPM ? Number(process.env.TPM) : 24000;
const TOKENS_PER_NOTE = process.env.TOKENS_PER_NOTE ? Number(process.env.TOKENS_PER_NOTE) : 8000;
const delay = (ms: number) => new Promise<void>((r) => setTimeout(r, ms));
let bucket = TPM;
let lastRefill = Date.now();
// Block until the bucket can afford one note's worth of input tokens, then debit
// it. Refills continuously at TPM/min; with CONCURRENCY workers it still caps the
// aggregate launch rate. No-op when TPM is 0.
async function pace() {
  if (!TPM) return;
  for (;;) {
    const now = Date.now();
    bucket = Math.min(TPM, bucket + ((now - lastRefill) / 60000) * TPM);
    lastRefill = now;
    if (bucket >= TOKENS_PER_NOTE) {
      bucket -= TOKENS_PER_NOTE;
      return;
    }
    await delay(Math.ceil(((TOKENS_PER_NOTE - bucket) / TPM) * 60000));
  }
}

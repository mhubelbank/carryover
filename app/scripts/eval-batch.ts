// Batch note generator for manual bulk quality review. Runs N passes × M
// students through the live pipeline with randomized (but reproducible, per
// SEED) activities / goals / prompting / trials, and writes every note plus its
// inputs to a markdown file for eyeballing. Synthetic data only — fake names,
// generic goals — so no PII.
//
//   ANTHROPIC_API_KEY=... GITHUB_TOKEN=... npx tsx scripts/eval-batch.ts [passes] [students]
//   npx tsx scripts/eval-batch.ts --dry            # build inputs only, no API
//   npx tsx scripts/eval-batch.ts --patch 5 10     # regenerate ONLY the errored
//     notes in the matching existing file (same SEED/PASSES/STUDENTS/MODEL); reuse
//     every good note as-is — cheap fix for rate-limited gaps, not a full rerun
//   SEED=7 npx tsx scripts/eval-batch.ts 4 8        # 4 passes of 8 students
//   npx tsx scripts/eval-batch.ts --no-news         # regular only (skip news-day)
//   npx tsx scripts/eval-batch.ts --news-only       # news-day only (skip regular)
//   MODEL=chatgpt-mini OPENAI_API_KEY=... npx tsx scripts/eval-batch.ts  # pick the model
//     (MODEL is a curated choice id; the model id lands in the output filename)
//   PIPELINE=claude npx tsx scripts/eval-batch.ts 5 10   # run the real PIPELINE

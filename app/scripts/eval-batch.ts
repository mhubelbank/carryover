const isErrorFinal = (t: string | undefined): boolean => t === undefined || t.startsWith("[generation error");

// Generate notes for `items`. In patch mode `reuse` carries the prior run's note
// text per index: good notes pass through untouched (no API call) and only the
// errored ones are regenerated, so a patch costs calls for the failures alone.
async function runGen(
  items: { p: number; s: BuiltStudent }[],
  prompts: PromptSet,
  golden: string,
  label: string,
  reuse?: string[],
) {
  const finals: { final: string }[] = items.map((_, i) =>
    reuse && !isErrorFinal(reuse[i]) ? { final: reuse[i]! } : { final: "" },
  );
  const todo = items.map((_, i) => i).filter((i) => !reuse || isErrorFinal(reuse[i]));
  console.log(`Generating ${todo.length}${reuse ? `/${items.length}` : ""} ${label} notes…`);
  let done = 0;
  await mapPool(todo, CONCURRENCY, async (idx) => {
    const { p, s } = items[idx]!;
    // Stay under the provider's per-minute token budget before firing this note.
    await pace();
    // Treat each pass as a different "week" so the ordering/closing-angle variety
    // rotates across passes (mirrors the app's week-to-week rotation).
    finals[idx] = await generateNote(apiKey, prompts, s.ctx, {
      ...(genPasses ? { passes: genPasses } : { provider: MODEL.provider, model: MODEL.modelId }),
      maxTokens: 1500,
      goldenExamples: golden,
      varietyNote: varietyNote(p),
    }).catch((e): { final: string } => ({ final: `[generation error: ${e instanceof Error ? e.message : String(e)}]` }));
    process.stdout.write(`\r  ${++done}/${todo.length}`);
  });
  if (todo.length) process.stdout.write("\n");
  return finals;
}

// Parse a prior output file back into per-note records, in build order, for one
// mode section ("Regular" | "News-day"). Mirrors renderSection's layout: each note
// is a "### P.S — …" heading, then "- " input bullets, then the note prose, then an
// optional "> ⚠" flag line, then a "---" separator. `inputs` is the bullet block
// (used to verify the seeded rebuild still lines up); `final` is the note text.
function parseModeNotes(fileText: string, mode: "Regular" | "News-day"): { inputs: string[]; final: string }[] {
  const start = fileText.indexOf(`# ${mode} mode`);
  if (start === -1) return [];
  const after = fileText.slice(start + 1);
  const nextHeader = after.indexOf("\n# ");
  const section = nextHeader === -1 ? after : after.slice(0, nextHeader);
  return section
    .split("\n### ")
    .slice(1) // drop the "## Pass 1" preamble before the first note
    .map((chunk) => {
      const end = chunk.indexOf("\n---");
      const body = (end === -1 ? chunk : chunk.slice(0, end)).split("\n");
      let i = 1; // body[0] is the heading remainder; skip the contiguous "- " bullets
      const inputs: string[] = [];
      while (i < body.length && body[i]!.startsWith("- ")) inputs.push(body[i++]!.slice(2));
      const final = body
        .slice(i)
        .filter((l) => !l.startsWith("> ⚠"))
        .join("\n")
        .trim();
      return { inputs, final };
    });
}

// Lift the original "# Tokens & cost" block verbatim — in patch mode the live meter
// only saw the regenerated notes, so the original block is the meaningful one.
function extractCostBlock(fileText: string): string {
  const start = fileText.indexOf("# Tokens & cost");
  if (start === -1) return "";
  const rest = fileText.slice(start);
  const end = rest.indexOf("\n---\n");
  return (end === -1 ? rest.trimEnd() : rest.slice(0, end)) + "\n\n---\n\n";
}

